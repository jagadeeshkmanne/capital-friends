import { useEffect } from 'react'
import { useAuth } from '../context/AuthContext'
import { useNavigate } from 'react-router-dom'
import { Shield } from 'lucide-react'

export default function LoginPage() {
  const { isAuthenticated, loading, error, signIn } = useAuth()
  const navigate = useNavigate()

  // Redirect if already authenticated
  useEffect(() => {
    if (isAuthenticated) {
      navigate('/', { replace: true })
    }
  }, [isAuthenticated, navigate])

  if (loading) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-[var(--bg-main)]">
        <div className="animate-pulse text-[var(--text-muted)] text-sm">Loading...</div>
      </div>
    )
  }

  return (
    <div className="min-h-screen flex items-center justify-center bg-[var(--bg-main)] px-4">
      <div className="w-full max-w-sm space-y-6 text-center">
        {/* Logo */}
        <div>
          <img src={`${import.meta.env.BASE_URL}logo-new.png`} alt="Capital Friends" className="h-16 w-auto mx-auto mb-4" />
          <h1 className="text-xl tracking-tight" style={{ fontFamily: "'Poppins', sans-serif" }}><span className="font-bold text-[var(--text-primary)]">Capital</span> <span className="font-extrabold text-emerald-400">Friends</span></h1>
          <p className="text-xs text-[var(--text-dim)] mt-1">Family Portfolio Management</p>
        </div>

        {/* Sign In Card */}
        <div className="bg-[var(--bg-card)] rounded-xl border border-[var(--border)] p-6 space-y-4">
          <p className="text-sm text-[var(--text-secondary)]">
            Sign in with your Google account to get started.
          </p>

          {/* Custom Google Sign-In Button */}
          <div className="flex justify-center">
            <button
              onClick={signIn}
              className="flex items-center gap-3 px-6 py-3 bg-white border border-gray-300 rounded-lg hover:bg-gray-50 active:bg-gray-100 transition-colors shadow-sm cursor-pointer"
            >
              <svg viewBox="0 0 24 24" width="20" height="20">
                <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 01-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.1z" fill="#4285F4"/>
                <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853"/>
                <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05"/>
                <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335"/>
              </svg>
              <span className="text-sm font-medium text-gray-700">Sign in with Google</span>
            </button>
          </div>

          {error && (
            <p className="text-xs text-[var(--accent-rose)] bg-rose-500/10 rounded-lg px-3 py-2">
              {error}
            </p>
          )}
        </div>

        {/* Privacy Note */}
        <div className="flex items-center justify-center gap-2 text-[var(--text-dim)]">
          <Shield size={12} />
          <p className="text-xs">
            Your data stays in your own Google Drive. We can't see it.
          </p>
        </div>
      </div>
    </div>
  )
}
